const addTwoNumbers = (one,two) =>{
    return one+two
}

const substractTwoNumbers = (one,two) =>{
    return one-two
}

const divideTwoNumbers = (one,two) =>{
    return one/two
}

const multiplyTwoNumbers = (one,two) =>{
    return one*two
}

export {addTwoNumbers, substractTwoNumbers, divideTwoNumbers, multiplyTwoNumbers}